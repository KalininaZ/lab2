import random

def угадай_число():
  """Игра 'Угадай число' с ограничением по диапазону и попыткам."""

  число = random.randint(1, 10)
  попытки = 0
  макс_попыток = 3

  print("Добро пожаловать в игру 'Угадай число'!")
  print("Я загадал число от 1 до 10. У вас есть 3 попытки.")

  while попытки < макс_попыток:
    try:
      предположение = int(input(f"Введите ваше предположение ({попытки + 1}/{макс_попыток}): "))
    except ValueError:
      print("Неверный ввод. Пожалуйста, введите целое число.")
      continue

    попытки += 1

    if предположение < число:
      print("Загаданное число больше.")
    elif предположение > число:
      print("Загаданное число меньше.")
    else:
      print(f"Поздравляю! Вы угадали число {число} за {попытки} попыток.")
      return

  print(f"У вас закончились попытки. Загаданное число было {число}.")

if __name__ == "__main__":
  угадай_число()

